package interfaces;

/**
 * La interfaz OlorFuerte proporciona un método para bloquear el rastro de un
 * olor fuerte en el juego.
 *
 * <p>
 * Autor: SergioQuiñonesMajuelo Version: 1.0 Fecha: 18-03-2024
 * </p>
 */
public interface OlorFuerte {

    /**
     * Bloquea el rastro de un olor fuerte en el juego.
     *
     * @return Una cadena que indica que el rastro ha sido bloqueado.
     */
    String bloquearRastro();
}
